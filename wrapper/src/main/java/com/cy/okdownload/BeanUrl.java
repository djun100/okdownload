package com.cy.okdownload;

public class BeanUrl {
        public String url;
        public String name;

        public String getUrl() {
            return url;
        }

        public BeanUrl setUrl(String url) {
            this.url = url;
            return this;
        }

        public String getName() {
            return name;
        }

        public BeanUrl setName(String name) {
            this.name = name;
            return this;
        }
    }